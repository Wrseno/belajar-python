#COMPARISON

varA = input("Variable a = ")
varB = input("Variable b = ")

if (varA == varB):
    print("Line 1 - varA is equal to varB")
else:
    print("Line 1 - varA is not equal to varB")

if (varA != varB):
    print("Line 2 - varA is not equal to varB")
else:
    print("Line 2 - varA is equal to varB")

if (varA < varB):
    print("Line 3 - varA is less than varB")
else: 
    print("Line 3 - varA is not less than varB")

if (varA > varB):
    print("Line 4 - varA is greater than varB")
else: 
    print("Line 4 - varA is not greater than varB")

if (varA <= varB):
    print("Line 5 - varA is either less than or equal to varB")
else: 
    print("Line 5 - varA is not either less than or equal to varB")

if (varA >= varB):
    print("Line 6 - varA is either greater than or equal to varB")
else: 
    print("Line 6 - varA is not either greater than or equal to varB")
