"""
    Contoh membuat program dari celcius ke fahrenheit
"""
suhuCelcius = int(input("Masukkan suhu derajat celcius : ")) # parsing int(input)
suhuFahrenheit = (suhuCelcius * (9/5)) + 32
print(f"{suhuCelcius} celcius = {suhuFahrenheit} Fahrenheit")

